async function updateProductDescriptions() {
  try {
    console.log('开始更新商品简介...\n');
    
    let updated = 0;
    let failed = 0;
    
    for (const [id, data] of Object.entries(productDescriptions)) {
      try {
        const product = await Product.findByPk(id);
        
        if (!product) {
          console.log(`⚠️  商品 ID ${id} 不存在，跳过`);
          failed++;
          continue;
        }
        
        if (product.name !== data.name) {
          console.log(`⚠️  商品 ID ${id} 名称不匹配（期望：${data.name}，实际：${product.name}），跳过`);
          failed++;
          continue;
        }
        
        await product.update({
          description: data.description
        });
        
        console.log(`✅ 已更新：${product.name} (ID: ${id})`);
        console.log(`   简介长度：${data.description.length} 字符`);
        updated++;
        
      } catch (error) {
        console.error(`❌ 更新商品 ID ${id} 失败：${error.message}`);
        failed++;
      }
    }
    
    console.log('\n' + '='.repeat(60));
    console.log(`更新完成！成功：${updated} 个，失败：${failed} 个`);
    console.log('='.repeat(60));
    
    process.exit(failed > 0 ? 1 : 0);
  } catch (error) {
    console.error('❌ 错误:', error.message);
    process.exit(1);
  }
}

updateProductDescriptions();
